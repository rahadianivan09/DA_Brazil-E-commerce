# ── Load Data ─────────────────────────────────────────────────────────────────
@st.cache_data
def load_data():
    main    = pd.read_csv('Dashboard/main_data.csv',
                          parse_dates=['order_purchase_timestamp'])
    payment = pd.read_csv('Dashboard/payment_data.csv',
                          parse_dates=['order_purchase_timestamp'])
    rfm     = pd.read_csv('Dashboard/rfm_data.csv')
    return main, payment, rfm

main_df, pay_df, rfm_df = load_data()

# ── Sidebar ───────────────────────────────────────────────────────────────────
with st.sidebar:
    st.title("🛒 E-Commerce")
    st.markdown("**Brazilian E-Commerce Public Dataset**")
    st.markdown("---")

    st.subheader("📅 Filter Periode")
    min_date = main_df['order_purchase_timestamp'].min().date()
    max_date = main_df['order_purchase_timestamp'].max().date()

    start_date = st.date_input("Dari", value=min_date,
                               min_value=min_date, max_value=max_date)
    end_date   = st.date_input("Sampai", value=max_date,
                               min_value=min_date, max_value=max_date)
    st.markdown("---")
    top_n = st.slider("Jumlah Top Kategori/State", 5, 15, 10)

# ── Filter ────────────────────────────────────────────────────────────────────
mask = (
    (main_df['order_purchase_timestamp'].dt.date >= start_date) &
    (main_df['order_purchase_timestamp'].dt.date <= end_date)
)
df = main_df[mask].copy()

pay_mask = (
    (pay_df['order_purchase_timestamp'].dt.date >= start_date) &
    (pay_df['order_purchase_timestamp'].dt.date <= end_date)
)
pay = pay_df[pay_mask].copy()

# ── Header ────────────────────────────────────────────────────────────────────
st.title("🛒 Brazilian E-Commerce Analytics Dashboard")
st.caption(f"Periode: {start_date} s/d {end_date}")
st.markdown("---")

# ── Metrics ───────────────────────────────────────────────────────────────────
rev_total = (
    df[['order_id']].drop_duplicates()
    .merge(pay_df[['order_id','total_payment']].drop_duplicates('order_id'),
           on='order_id', how='left')['total_payment'].sum()
)

