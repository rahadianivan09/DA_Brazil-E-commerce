    # insight
    corr_order = df['delivery_delay_days'].corr(df['review_score'])
    late_n     = (df['delivery_delay_days'] > 0).sum()
    st.info(f"""
    📌 **Insight:** Korelasi order-level delay vs review = **{corr_order:.3f}** (negatif).
    {late_n:,} orders ({late_n/len(df)*100:.1f}%) terlambat pada periode ini.
    """)

