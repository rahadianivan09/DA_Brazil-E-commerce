        legend_labels = [
            f'{l} ({v:.1f}%)' for l, v in zip(
                labels,
                pay_type['total_revenue'] /
                pay_type['total_revenue'].sum() * 100
            )
        ]
        ax.legend(wedges, legend_labels,
                  title='Payment Method',
                  loc='lower center',
                  bbox_to_anchor=(0.5, -0.15),
                  ncol=2, fontsize=9, title_fontsize=9)
        ax.set_title('Revenue Share\nby Payment Method', fontweight='bold', pad=15)
